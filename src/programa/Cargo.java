package programa;

public class Cargo 
{
	private String nombre; 
	private String familia;
	private String gupo_ocupacional;
	private String nivel_funcional;
	private String grupo_laboral;
	
	/* ******************************************************************************************************************************************** */
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	/* ******************************************************************************************************************************************** */
	public String getFamilia() {
		return familia;
	}
	public void setFamilia(String familia) {
		this.familia = familia;
	}
	
	/* ******************************************************************************************************************************************** */
	public String getGupo_ocupacional() {
		return gupo_ocupacional;
	}
	public void setGupo_ocupacional(String gupo_ocupacional) {
		this.gupo_ocupacional = gupo_ocupacional;
	}
	
	/* ******************************************************************************************************************************************** */
	public String getNivel_funcional() {
		return nivel_funcional;
	}
	public void setNivel_funcional(String nivel_funcional) {
		this.nivel_funcional = nivel_funcional;
	}
	
	/* ******************************************************************************************************************************************** */
	public String getGrupo_laboral() {
		return grupo_laboral;
	}
	public void setGrupo_laboral(String grupo_laboral) {
		this.grupo_laboral = grupo_laboral;
	}
	
	
}
